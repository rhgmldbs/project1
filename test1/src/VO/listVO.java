package VO;


public class listVO {
	private int lno;
	private String lid;
	private String lpw;
	private String lname;
	private String lgender;
	private String lmail;
	private String ltell;
	
	public int getLno() {
		return lno;
	}
	public void setLno(int lno) {
		this.lno = lno;
	}
	public String getLid() {
		return lid;
	}
	public void setLid(String lid) {
		this.lid = lid;
	}
	public String getLpw() {
		return lpw;
	}
	public void setLpw(String lpw) {
		this.lpw = lpw;
	}
	public String getLname() {
		return lname;
	}
	public void setLname(String lname) {
		this.lname = lname;
	}
	public String getLgender() {
		return lgender;
	}
	public void setLgender(String lgender) {
		this.lgender = lgender;
	}
	public String getLmail() {
		return lmail;
	}
	public void setLmail(String lmail) {
		this.lmail = lmail;
	}
	public String getLtell() {
		return ltell;
	}
	public void setLtell(String ltell) {
		this.ltell = ltell;
	}

	
}
